	PSpinnakerDevice CDriver_Spinnaker::FindDeviceConnection(const std::string & sIdentifier)
	{
		LibMCDriver_SpinnakerHandle hDevice = nullptr;
		CheckError(m_pWrapper->m_WrapperTable.m_Driver_Spinnaker_FindDeviceConnection(m_pHandle, sIdentifier.c_str(), &hDevice));
		
		if (!hDevice) {
			CheckError(LIBMCDRIVER_SPINNAKER_ERROR_INVALIDPARAM);
		}
		return std::make_shared<CSpinnakerDevice>(m_pWrapper, hDevice);
	}
	
