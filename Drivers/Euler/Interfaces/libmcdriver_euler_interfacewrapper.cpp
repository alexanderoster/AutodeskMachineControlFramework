LibMCDriver_EulerResult libmcdriver_euler_getlasterror(LibMCDriver_Euler_Base pInstance, const LibMCDriver_Euler_uint32 nErrorMessageBufferSize, LibMCDriver_Euler_uint32* pErrorMessageNeededChars, char * pErrorMessageBuffer, bool * pHasError)
{
	IBase* pIBaseClass = nullptr;

	try {
		if ( (!pErrorMessageBuffer) && !(pErrorMessageNeededChars) )
			throw ELibMCDriver_EulerInterfaceException (LIBMCDRIVER_EULER_ERROR_INVALIDPARAM);
		if (pHasError == nullptr)
			throw ELibMCDriver_EulerInterfaceException (LIBMCDRIVER_EULER_ERROR_INVALIDPARAM);
		IBase* pIBaseClassInstance = (IBase *)pInstance;
		IBase* pIInstance = dynamic_cast<IBase*>(pIBaseClassInstance);
		if (!pIInstance)
			throw ELibMCDriver_EulerInterfaceException (LIBMCDRIVER_EULER_ERROR_INVALIDCAST);
		
		std::string sErrorMessage("");
		*pHasError = CWrapper::GetLastError(pIInstance, sErrorMessage);

		if (pErrorMessageNeededChars)
			*pErrorMessageNeededChars = (LibMCDriver_Euler_uint32) (sErrorMessage.size()+1);
		if (pErrorMessageBuffer) {
			if (sErrorMessage.size() >= nErrorMessageBufferSize)
				throw ELibMCDriver_EulerInterfaceException (LIBMCDRIVER_EULER_ERROR_BUFFERTOOSMALL);
			for (size_t iErrorMessage = 0; iErrorMessage < sErrorMessage.size(); iErrorMessage++)
				pErrorMessageBuffer[iErrorMessage] = sErrorMessage[iErrorMessage];
			pErrorMessageBuffer[sErrorMessage.size()] = 0;
		}
		return LIBMCDRIVER_EULER_SUCCESS;
	}
	catch (ELibMCDriver_EulerInterfaceException & Exception) {
		return handleLibMCDriver_EulerException(pIBaseClass, Exception);
	}
	catch (std::exception & StdException) {
		return handleStdException(pIBaseClass, StdException);
	}
	catch (...) {
		return handleUnhandledException(pIBaseClass);
	}
}

