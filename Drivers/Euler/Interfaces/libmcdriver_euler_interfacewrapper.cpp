LibMCDriver_EulerResult libmcdriver_euler_createdriver(const char * pName, const char * pType, LibMCEnv_DriverEnvironment pDriverEnvironment, LibMCDriver_Euler_Driver * pInstance)
{
	IBase* pIBaseClass = nullptr;

	try {
		if (pName == nullptr)
			throw ELibMCDriver_EulerInterfaceException (LIBMCDRIVER_EULER_ERROR_INVALIDPARAM);
		if (pType == nullptr)
			throw ELibMCDriver_EulerInterfaceException (LIBMCDRIVER_EULER_ERROR_INVALIDPARAM);
		if (pInstance == nullptr)
			throw ELibMCDriver_EulerInterfaceException (LIBMCDRIVER_EULER_ERROR_INVALIDPARAM);
		std::string sName(pName);
		std::string sType(pType);
		LibMCEnv::PDriverEnvironment pIDriverEnvironment = std::make_shared<LibMCEnv::CDriverEnvironment>(CWrapper::sPLibMCEnvWrapper.get(), pDriverEnvironment);
		CWrapper::sPLibMCEnvWrapper->AcquireInstance(pIDriverEnvironment.get());
		if (!pIDriverEnvironment)
			throw ELibMCDriver_EulerInterfaceException (LIBMCDRIVER_EULER_ERROR_INVALIDCAST);
		
		IBase* pBaseInstance(nullptr);
		pBaseInstance = CWrapper::CreateDriver(sName, sType, pIDriverEnvironment);

		*pInstance = (IBase*)(pBaseInstance);
		return LIBMCDRIVER_EULER_SUCCESS;
	}
	catch (ELibMCDriver_EulerInterfaceException & Exception) {
		return handleLibMCDriver_EulerException(pIBaseClass, Exception);
	}
	catch (std::exception & StdException) {
		return handleStdException(pIBaseClass, StdException);
	}
	catch (...) {
		return handleUnhandledException(pIBaseClass);
	}
}


